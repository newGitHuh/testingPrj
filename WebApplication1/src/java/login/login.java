    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        PreparedStatement ps = null;
        ResultSet rs = null;
        PrintWriter o = response.getWriter();
        String user, pass;
        boolean loginYet = false;
        user = (String) request.getParameter("user");
        pass = (String) request.getParameter("pass");
        String query = "select * from account where [user] = ? and pass = ?";

        try {
            Connection db = DBUtils.getConnection();
            ps = db.prepareStatement(query);
            ps.setString(1, user);
            ps.setString(2, pass);
            rs = ps.executeQuery();
            if (rs.next()) {
                HttpSession session = request.getSession();
                session.setAttribute("user", user);
                loginYet=true;
                request.setAttribute("loginConfirm", loginYet);
                RequestDispatcher rq = request.getRequestDispatcher("home.jsp");
                
                rq.forward(request, response);
            }
        } catch (ClassNotFoundException | SQLException e) {
            o.print(e.getMessage());
        }
    }

